// 根据分数查位次（取该分数对应的累计人数）
window.scoreToRank = function(score, year) {
  const map = window.SCORE_RANK_MAP[year];
  if (!map) return null;
  const s = parseInt(score);
  if (isNaN(s)) return null;
  // 精确匹配或向下取最近的分数
  if (map[s] !== undefined) return map[s];
  // 向下查找最近的键
  const keys = Object.keys(map).map(Number).sort((a,b)=>b-a);
  for (const k of keys) {
    if (s >= k) return map[k];
  }
  return null;
};

// 根据位次查同位分
window.rankToScore = function(rank, year) {
  const map = window.SCORE_RANK_MAP[year];
  if (!map) return null;
  const r = parseInt(rank);
  if (isNaN(r)) return null;
  let bestScore = null;
  for (const [score, cumRank] of Object.entries(map)) {
    if (cumRank >= r) {
      if (bestScore === null || parseInt(score) > bestScore) {
        bestScore = parseInt(score);
      }
    }
  }
  return bestScore;
};
