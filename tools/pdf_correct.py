for g in data['groups']:
    name = g['schoolName']
    gc = g['groupCode']
    if name in PDF_DATA and gc in PDF_DATA[name]:
        pdf_hist = PDF_DATA[name][gc]
        new_hist = []
        for year, score, rank in pdf_hist:
            if score != '--':
                new_hist.append({'year': year, 'rank': rank, 'score': score})
        if new_hist:
            old_hist = g['config'].get('history', [])
            old_str = '|'.join(h.get('score','?') for h in old_hist[:3])
            new_str = '|'.join(h['score'] for h in new_hist[:3])
            if old_str != new_str:
                g['config']['history'] = new_hist
                updated += 1
                print('  {:20s} {:4s}: {} -> {}'.format(name, gc, old_str, new_str))

